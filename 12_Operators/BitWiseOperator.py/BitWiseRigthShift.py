# Bitwise Right Shift (>>)

print(20 >> 5)

# Step 1: Convert the decimal number into binary.

# 20 = 10100

# Step 2: Shift all bits 5 positions to the right.

# Original : 10100

# Shift 1 : 01010
# Shift 2 : 00101
# Shift 3 : 00010
# Shift 4 : 00001
# Shift 5 : 00000

# Step 3: Convert the binary result back to decimal.

# 00000 = 0

# Therefore,

# 20 >> 5 = 0